<div class="faq">
<h2>When should we set up our wedding registry?</h2>
<p>We suggest that you book a consultation with us prior to sending out your wedding invitations, so that you can include our small notification cards. However, we recommend that the second consultation/shopping excursion is done a maximum of 6 weeks before the date of your wedding to avoid selected items becoming unavailable.</p>
</div>

<div class="faq">
<h2>What is the first step?</h2>
<p>Bespoke prides itself on its personal, tailor-made service. We will help you create a wedding registry that reflects you as a couple. You will therefore need to book an appointment to meet with us to discuss what you would like from your registry.<a href="<?php echo $this->createUrl("site/contactUs"); ?> " > Contact us</a> to set up your appointment either by <a href="mailto: laura@bespokeregistry.co.za">email</a> or telephone.</p>
</div>

<div class="faq">
<h2>Why do we need two consultations?</h2>
<p>During the first consultation we will run you through an extensive checklist to give us a clear indication of what exactly it is you need. Once we have an idea of your requirements you can start selecting items such as kitchen appliances and cookware. The second consultation will be scheduled 4-6 weeks prior to your wedding date. During this consultation, we’ll take you around to various shops and you will be able to pick out the more personal items. </p>
</div>


<div class="faq">
<h2>We would like to have some larger items on our registry, can our guests contribute towards them?</h2>
<p>Yes, absolutely. More and more people are getting married and combining two homes into one, so this is quite a common request. You can select a large item of furniture that you may need, or a kitchen appliance such as a dishwasher, and your guests will then be able to contribute any amount towards this on your registry page. You will be able to see who has contributed towards the item on your guest purchase report. If there is an outstanding balance on the item when you close your registry you will be able to either pay in the remaining amount, or allocate the money contributed to other things on your list.</p>
</div>


<div class="faq">
<h2>Is there an admin fee?</h2>
<p>Yes, we charge a R500 once-off fee for our services. This is to be paid after the first consultation. This fee covers the two consultations, development of your webpage, collection of all your gifts, and two deliveries within Cape Town.</p>
</div>


<div class="faq">
<h2>How will our friends and family know where our registry is?</h2>
<p>We provide you with small notification cards that discreetly advise where you have placed your wedding registry. These cards may be enclosed with the wedding invitations. Alternatively, you could include the details on your own wedding information or website.</p>
</div>


<div class="faq">
<h2>How can my guests pay for the gifts?</h2>
<p>We process all payments through VCS secure payment gateway. We accept both Visa and MasterCard debit and credit cards. If you would like to pay via EFT then please contact us and we will make arrangements.</p>
</div>


<div class="faq">
<h2>How will we know who bought us what?</h2>
<p>You can view the progress of your registry 24 hours a day on our website. You will be given a password at registration enabling you to log in. All gifts purchased will be listed with the details of the guest who brought it and their personal message. Your gifts will also be delivered with gift tags stating whom the gift is from and their message.</p>
</div>


<div class="faq">
<h2>Can we change our mind about any of the gifts on our registry?</h2>
<p>Absolutely! Your registry is completely flexible until you confirm your final order of gifts after the wedding. You may wish to order your gifts exactly as your guests have selected them or you may prefer to make some changes i.e. not going ahead with some items and crediting them towards others on your list. This can be useful to complete services of china, cutlery and glassware. </p>
</div>



<div class="faq">
<h2>How will our gifts be delivered?</h2>
<p>In order to allow flexibility on the final selection of presents, we don’t order your gifts when your guests purchase them but wait until we have received confirmation of your order after your wedding. It is only then that we order the gifts from our suppliers on your behalf. You will receive two complimentary deliveries within Cape Town arranged around your schedule. The first delivery is on average 2 weeks from the time of placing your order. Certain items may not be ready, especially framed artwork, and made to order items such as furniture. Any additional items that are not delivered with the first two deliveries can be collected from our offices in Claremont. All your gifts will arrive beautifully wrapped and labelled.</p>
</div>







